	translator VARCHAR(255),
	publisher VARCHAR(255),
	published_date VARCHAR(255),
	sell_price VARCHAR(50),
	original_price VARCHAR(50),
	arrived_date VARCHAR(50),
	klover_rating VARCHAR(50),
	klover_vote_count VARCHAR(50),
	rating VARCHAR(50),
	review_count VARCHAR(50),
	page_count VARCHAR(50),
	size VARCHAR(50),
	original_title VARCHAR(255),
	breadcrumbs VARCHAR(1000),
	hashtags VARCHAR(1000),
	keywords VARCHAR(1000),
